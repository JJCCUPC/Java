
package ClaseObjetos;

public class Persona {
    public String id;
     public String apellido;
      public String nombre;
       int edad;
       
    public Persona(){
        
    }

    public Persona(String id, String apellido, String nombre, int edad) {
        this.id = id;
        this.apellido = apellido;
        this.nombre = nombre;
        this.edad = edad;
    }

}
